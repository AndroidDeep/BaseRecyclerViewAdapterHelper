    /**
     * To bind different types of holder and solve different the bind events
     *
     * @param holder
     * @param position
     * @see #getDefItemViewType(int)
     */
    @Override
    public void onBindViewHolder(@NonNull ViewHolder holder, int position) {
        //Add up fetch logic, almost like load more, but simpler.
        int viewType = holder.getItemViewType();

        switch (viewType) {
            case LOADING_VIEW:
            case FOOTER_VIEW:
            case EMPTY_VIEW:
            case HEADER_VIEW:
                break;
            default:
                convert(holder, getItem(position - getHeaderCount()));
                break;
        }
    }

    /**
     * To bind different types of holder and solve different the bind events
     *
     * the ViewHolder is currently bound to old data and Adapter may run an efficient partial
     * update using the payload info.  If the payload is empty,  Adapter run a full bind.
     *
     * @param holder The ViewHolder which should be updated to represent the contents of the
     *               item at the given position in the data set.
     * @param position The position of the item within the adapter's data set.
     * @param payloads A non-null list of merged payloads. Can be empty list if requires full
     *                 update.
     * @see #getDefItemViewType(int)
     */
    @Override
    public void onBindViewHolder(@NonNull ViewHolder holder, int position, @NonNull List<Object> payloads) {
        if (payloads.isEmpty()) {
            onBindViewHolder(holder, position);
            return;
        }
        int viewType = holder.getItemViewType();

        switch (viewType) {
            case LOADING_VIEW:
            case HEADER_VIEW:
            case EMPTY_VIEW:
            case FOOTER_VIEW:
                break;
            default:
                convertPayloads(holder, getItem(position - getHeaderCount()), payloads);
                break;
        }
    }


    protected void bindViewClickListener(final ViewHolder viewHolder) {
        if (viewHolder == null) {
            return;
        }
        final View view = viewHolder.itemView;
        if (getOnItemClickListener() != null) {
            view.setOnClickListener(new View.OnClickListener() {
                @Override
                public void onClick(View v) {
                    int position = viewHolder.getAdapterPosition();
                    if (position == RecyclerView.NO_POSITION) {
                        return;
                    }
                    position -= getHeaderCount();
                    setOnItemClick(v, position);
                }
            });
        }
        if (getOnItemLongClickListener() != null) {
            view.setOnLongClickListener(new View.OnLongClickListener() {
                @Override
                public boolean onLongClick(View v) {
                    int position = viewHolder.getAdapterPosition();
                    if (position == RecyclerView.NO_POSITION) {
                        return false;
                    }
                    position -= getHeaderCount();
                    return setOnItemLongClick(v, position);
                }
            });
        }
    }

